export const translations = { de, en };
export type Translations = typeof de;

// ─── Context ──────────────────────────────────────────────────────────────────

import React from "react";

type LangCtxValue = { lang: Lang; setLang: (l: Lang) => void };

const LangCtx = React.createContext<LangCtxValue>({ lang: "de", setLang: () => {} });

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = React.useState<Lang>("de");

  React.useEffect(() => {
    try {
      const stored = localStorage.getItem("app_lang") as Lang | null;
      if (stored === "de" || stored === "en") setLangState(stored);
    } catch {}
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    try { localStorage.setItem("app_lang", l); } catch {}
  }

  return React.createElement(LangCtx.Provider, { value: { lang, setLang } }, children);
}

export function useLang() {
  return React.useContext(LangCtx);
}

export function useT() {
  const { lang } = useLang();
  return translations[lang];
}
